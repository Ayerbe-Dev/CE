#[fighter_frame( agent = FIGHTER_KIND_MARTH )]
pub fn marth_functions(fighter: &mut L2CFighterCommon) {
	unsafe {
		let module_accessor = app::sv_system::battle_object_module_accessor(fighter.lua_state_agent);
		let motion_kind = MotionModule::motion_kind(module_accessor);
		let fighter_kind = app::utility::get_kind(module_accessor) as i32;

		if fighter_kind == *FIGHTER_KIND_MARTH {
			if motion_kind == hash40("final_start") || motion_kind == hash40("final_air_start") {
				MARTH_LOOP_COUNT[get_player_number(module_accessor)] = 0;
			}
			if motion_kind == hash40("final_dash") && estimate_frame(module_accessor, 4.0) {
				MARTH_LOOP_COUNT[get_player_number(module_accessor)] += 1;
			}
		}
	}
}

